    /**
     * 根据ID获取文档
     */
    public static KnowledgeDocument getDocumentById(String id) {
        return getAllDocuments().stream()
            .filter(doc -> doc.getId().equals(id))
            .findFirst()
            .orElse(null);
    }
    
    /**
     * 根据分类获取文档
     */
    public static List<KnowledgeDocument> getDocumentsByCategory(String category) {
        return getAllDocuments().stream()
            .filter(doc -> doc.getCategory().equals(category))
            .collect(java.util.stream.Collectors.toList());
    }
